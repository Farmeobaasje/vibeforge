  // ── Step 2: Update the open InterviewQuestion for this topic with the answer ──
  mem.questions = mem.questions.map((q) =>
    q.topic === topic && q.answer === ""
      ? { ...q, answer, answeredAt: new Date().toISOString() }
      : q,
  );

  // ── Step 3: Feed to Conversation Engine ──
  const engine = new ConversationEngine();

  // Restore previous engine state if provided
  if (engineState) {
    engine.restoreState(engineState);
  }

  const engineResult = engine.processAnswer(mem, topic, answer);

  // ── Step 4: If interview is done, run requirements and return ──
