// ──────────────────────────────────────────────
// interviewSession — Epic 23.4
// Integration layer between Conversation Engine,
// Interview Agent, and Requirements Builder.
//
// This is a stateless service that orchestrates
// the full interview cycle:
//
//   ConversationEngine → InterviewAgent → LLM
//       → parse → memory update → RequirementsBuilder
//
// It holds NO state, NO React hooks, NO storage.
// Memory is returned as a new object — the caller
// is responsible for persisting it.
// ──────────────────────────────────────────────

import type { Orchestrator } from "../../orchestrator";
import type { ChatMessage } from "../gateway";
import type { ConversationMemory } from "../../models/conversationMemory";
import { createEmptyConversationMemory } from "../../models/conversationMemory";
import type {
  InterviewState,
  EngineResult,
  EngineConfig,
  CompletionBreakdown,
  RequirementsBuildResult,
} from "../../orchestrator";
import {
  ConversationEngine,
  buildRequirements,
} from "../../orchestrator";
import { topicQuestion, topicLabel } from "../../orchestrator/interviewTopics";
import type {
  AgentQuestion,
  InterviewAgentConfig,
  InterviewAgentResult,
} from "./interviewAgentTypes";
import type { PlannedQuestion } from "../../orchestrator/interviewPlanner";
