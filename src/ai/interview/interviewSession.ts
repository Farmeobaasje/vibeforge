// ── Core: skip the current topic ──────────────

/**
 * Skip the current topic and move to the next question.
 *
 * @param memory       - The current conversation memory
 * @param config       - Session config
 * @param engineState  - Optional previous engine state to restore (for state persistence across calls)
 * @returns            - The session result with the next question
 */
export async function skipTopic(
  memory: ConversationMemory,
  config: InterviewSessionConfig,
  engineState?: InterviewState,
): Promise<InterviewSessionResult> {
  const mem = cloneMemory(memory);

  const engine = new ConversationEngine(config.engineConfig);

  // Restore previous engine state if provided, so state persists across calls
  if (engineState) {
    engine.restoreState(engineState);
  }

  const engineResult = engine.skipTopic(mem);

  if (!engineResult.nextQuestion) {
    const requirements = buildRequirements(mem);
    return {
      done: true,
      isFollowUp: false,
      memory: mem,
      state: engineResult.state,
      requirements,
      completion: engineResult.completion,
    };
  }

  const agent = new InterviewAgent(config.orchestrator, config.agentConfig);
  const agentResult = await agent.ask(toAgentQuestion(engineResult), mem);

  if (!agentResult.success) {
    const requirements = buildRequirements(mem);
    return {
      done: false,
      isFollowUp: false,
      topic: engineResult.nextQuestion.topic,
      memory: mem,
      state: engineResult.state,
      requirements,
      completion: engineResult.completion,
      error: agentResult.error ?? "LLM call failed",
      messages: agentResult.messages,
    };
  }

  mem.messages.push({
    id: uid(),
    role: "assistant",
    content: agentResult.answer.content,
    createdAt: new Date().toISOString(),
  });

  mem.questions.push({
    id: uid(),
    topic: engineResult.nextQuestion.topic,
    question: agentResult.answer.content,
    answer: "",
    confidence: "low",
    skipped: false,
    createdAt: new Date().toISOString(),
    answeredAt: null,
  });

  const requirements = buildRequirements(mem);

  return buildResult(engineResult, mem, requirements, agentResult);
}
